# Operators in python are special symbols we use to perform mathematical operations 
# + add 
# - subtract
# * multiply
# / standard division 
# // integer division
# % Modulo operation (a form of division)

#Standard division /
# 7/2 = 3.5
# always produces a FLOAT #
# for example 10/2 = 5.0 not 5

# Integer division //
# Will always return an integer
# for example 6//2 = 3 not 3.0
#7//2 = 3 not 3.5
# This type of division will always return the lowest whole number 

#Modulo divison %
#This is solving for the remainder
# 6 % 2 = 0 because 6 = 2 * 3 + 0
# 7 % 2 = 1 because 7 = 2 * 3 + 1

# KEEP IN MIND THAT YOU CANNOT DIVIDE BY 0 WITH ANY OF THE OPERATORS /, //, % YOU WILL RECIEVE A "ZeroDivisionError"

# Power operator (**)
#2 ** 3 = 8 because 2^3 = 8 and because 2 * 2 * 2 = 8
# If you use 2 integers for +, -, *, ** you will get back an integer but if you use 1 float or more you will get back a float
# for example 3 + 2 = 5 but 3 + 2.0 = 5.0 and 3.0 + 2.0 = 5.0 as well
